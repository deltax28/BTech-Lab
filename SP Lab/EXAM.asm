.MODEL SMALL
.STACK 256H

.DATA
MSG1 DB "PK$"
MSG2 DB 0AH,0DH,"LENGTH $"
MSG3 DB 0AH,0DH,"EVEN $"
MSG4 DB 0AH,0DH,"ODD $"

.CODE

MAIN:

MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1			;SHOW STRING
MOV AH,09H
INT 21H

MOV CL,00H
LEA SI,MSG1
;MOV AL,[SI]
MOV AL,00H
LP:
	MOV AL,[SI]
	CMP AL,24H 		;END OF STRING
	JZ FIN
	INC CL
	INC SI
	JMP LP
FIN:

LEA DX,MSG2			;SHOW LENGTH
MOV AH,09H
INT 21H

MOV DL,CL			;LENGTH VALUE
ADD DL,30H
MOV AH,02H
INT 21H

MOV AH,00H
MOV AL,CL			
MOV DL,02H
DIV DL

CMP AH,00H
JZ EV

LEA DX,MSG4				;ODD
MOV AH,09H
INT 21H
JMP EXT
EV:
	LEA DX,MSG3			;EVEN
	MOV AH,09H
	INT 21H
EXT:
	MOV AH,4CH
	INT 21H
END MAIN	



	
	
	